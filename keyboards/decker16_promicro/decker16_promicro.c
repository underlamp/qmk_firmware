bool encoder_update_kb(uint8_t index, bool clockwise) {
  if (index == 0) {
    if (clockwise) {
      tap_code_delay(KC_VOLU, 10);
    } else {
      tap_code_delay(KC_VOLD, 10);
    }
  }

  return false;
}
